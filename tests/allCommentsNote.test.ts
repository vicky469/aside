test("buildAllCommentsNoteContent labels page notes and orphaned notes", () => {
    const content = buildAllCommentsNoteContent("dev", [
        createComment({
            id: "page-note",
            anchorKind: "page",
            selectedText: "Note",
            startLine: 0,
            startChar: 0,
            endLine: 0,
            endChar: 0,
        }),
        createComment({
            id: "orphan-note",
            orphaned: true,
            selectedText: "missing text",
            startLine: 10,
        }),
    ]);

    assert.match(content, /\[page note · 1\]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=page-note\)/);
    assert.match(content, /\[orphaned · missing text\]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=orphan-note\)/);
});

test("buildAllCommentsNoteContent numbers page notes per file when no mentioned page label is available", () => {
    const content = buildAllCommentsNoteContent("dev", [
        createComment({
            id: "page-note-1",
            filePath: "Folder/Note.md",
            anchorKind: "page",
            selectedText: "Note",
            startLine: 0,
            startChar: 0,
            endLine: 0,
            endChar: 0,
            timestamp: 1,
        }),
        createComment({
            id: "page-note-2",
            filePath: "Folder/Note.md",
            anchorKind: "page",
            selectedText: "Note",
            startLine: 0,
            startChar: 0,
            endLine: 0,
            endChar: 0,
            timestamp: 2,
        }),
        createComment({
            id: "page-note-other-file",
            filePath: "Folder/Other.md",
            anchorKind: "page",
            selectedText: "Other",
            startLine: 0,
            startChar: 0,
            endLine: 0,
            endChar: 0,
            timestamp: 3,
        }),
    ]);

    assert.match(content, /\*\*Folder\/Note\.md\*\*[\s\S]*\[page note · 1\]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=page-note-1\)/);
    assert.match(content, /\*\*Folder\/Note\.md\*\*[\s\S]*\[page note · 2\]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=page-note-2\)/);
    assert.match(content, /\*\*Folder\/Other\.md\*\*[\s\S]*\[page note · 1\]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FOther\.md&commentId=page-note-other-file\)/);
});

test("buildAllCommentsNoteContent expands mentioned pages and collapses duplicates per comment", () => {
    const content = buildAllCommentsNoteContent("dev", [
        createComment({
            id: "page-note",
            anchorKind: "page",
            selectedText: "Folder/Note",
            comment: "See [[Another page]] and [[Another page|Alias]] plus [[Third page]].",
            startLine: 0,
            startChar: 0,
            endLine: 0,
            endChar: 0,
        }),
        createComment({
            id: "anchored-note",
            selectedText: "hello",
            comment: "Track [[Roadmap]] for this",
            startLine: 4,
        }),
    ], {
        getMentionedPageLabels: (comment) => {
            if (comment.id === "page-note") {
                return ["Another page", "Another page", "Third page"];
            }

            if (comment.id === "anchored-note") {
                return ["Roadmap"];
            }

            return [];
        },
    });

    assert.match(content, /- \[page note · Another page]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=page-note\)/);
    assert.match(content, /- \[page note · Third page]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=page-note\)/);
    assert.match(content, /- \[hello · Roadmap]\(obsidian:\/\/side-note2-comment\?vault=dev&file=Folder%2FNote\.md&commentId=anchored-note\)/);
    assert.equal(content.match(/page note · Another page/g)?.length ?? 0, 1);
});

test("isAllCommentsNotePath matches the generated note path", () => {
    assert.equal(isAllCommentsNotePath(ALL_COMMENTS_NOTE_PATH), true);
    assert.equal(isAllCommentsNotePath(LEGACY_ALL_COMMENTS_NOTE_PATH), true);
    assert.equal(isAllCommentsNotePath("notes/custom index.md", "notes/custom index.md"), true);
    assert.equal(isAllCommentsNotePath("Random.md"), false);
});

test("normalizeAllCommentsNotePath keeps the default and adds md when needed", () => {
    assert.equal(normalizeAllCommentsNotePath(""), ALL_COMMENTS_NOTE_PATH);
    assert.equal(normalizeAllCommentsNotePath("notes/custom index"), "notes/custom index.md");
    assert.equal(normalizeAllCommentsNotePath("notes/custom index.md"), "notes/custom index.md");
});

test("normalizeAllCommentsNoteImageUrl keeps the default when blank", () => {
    assert.equal(normalizeAllCommentsNoteImageUrl(""), ALL_COMMENTS_NOTE_IMAGE_URL);
    assert.equal(normalizeAllCommentsNoteImageUrl(" https://example.com/header.webp "), "https://example.com/header.webp");
});

test("normalizeAllCommentsNoteImageCaption keeps the default when missing and allows blank", () => {
    assert.equal(normalizeAllCommentsNoteImageCaption(null), ALL_COMMENTS_NOTE_IMAGE_CAPTION);
    assert.equal(normalizeAllCommentsNoteImageCaption(" Custom caption "), "Custom caption");
    assert.equal(normalizeAllCommentsNoteImageCaption(" "), "");
});
